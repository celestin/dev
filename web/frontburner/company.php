    <p>As from 1st January 2007 the following applies to:</p>

    <p>Business Stationery</p>

    <p>Whether in hard copy, electronic or any other form:</p>

    <p>A company must state its name, in legible lettering, on the following -</p>

    <ul>
      <li>all the company&rsquo;s business letters, order forms; </li>
      <li>all its notices and other official publications; </li>
      <li>all bills of exchange, promissory notes, endorsements, cheques and orders for money or goods purporting to be signed by, or on behalf of, the company; </li>
      <li>all its bills of parcels, invoices, receipts and letters of credit </li>
      <li>on all its websites</li>
    </ul>

    <p>On all of its business letters, order forms or any of the company&rsquo;s web sites, the company must show in legible lettering -</p>

    <ul>
      <li>its place of registration </li>
      <li>registered number </li>
      <li>its registered office address </li>
      <li>and if it is being wound up, that fact</li>
    </ul>

    <p>Whenever an email is used where its paper equivalent would be caught by the stationery requirements then that email is also subject to the requirements</p>

    <p>The above also applies to Limited Liability Partnerships.</p>

    <p><a href="http://www.opsi.gov.uk/si/si2006/20063429.htm" target="_blank">Regulations bringing these changes into force can be found by clicking here.</a></p>


    <h2>More information</h2>

    <blockquote>Failure to do so is a breach of the Companies Act 1985, and you could be liable to a substantial fine for authorising the issuance of a non-compliant document. </blockquote>

    <p>Companies to update websites and email footers before 2007 Companies in the UK must include certain regulatory information on their websites and in their email footers before 1 January 2007 or they will breach the Companies Act and risk a fine. </p>

    <p>As a result of an update to the legislation of 1985, every company should list its company registration number, place of registration, and registered office address on its website. The information, which must be in legible characters, should also appear on order forms and in emails. Such information is already required on &quot;business letters&quot; but the duty is being extended to websites, order forms and electronic documents.</p>

    <p>The information is likely to appear in the footer of every email sent from a company, to avoid having to decide whether each email amounts to a &quot;business letter&quot; or not. Many companies do this already because the term &quot;business letters&quot; was thought likely to include emails even without this new clarification.</p>

    <p>For websites, contrary to the fears of some, the specified information does not need to appear on every page. Again, many websites will already list the required information, perhaps on their "About us" or "Legal info" pages.</p>

    <h3>Information that must be on your website </h3>

    <p>The following is the minimum information that must be on any company&rsquo;s website:</p>

    <blockquote>The name, geographic address and email address of the service provider. The name of the organisation with which the customer is contracting must be given. This might differ from the trading name. Any such difference should be explained - e.g. &quot;XYZ.com is the trading name of XYZ Enterprises Limited.&quot; </blockquote>
    <p>It is not sufficient to include a &lsquo;contact us&rsquo; form without also providing an email address and geographic address somewhere easily accessible on the site. A PO Box is unlikely to suffice as a geographic address; but a registered office address would. If the business is a company, the registered office address must be included.</p>

    <ul>
      <li>If a company, the company&rsquo;s registration number should be given and, under the Companies Act, the place of registration should be stated (e.g. &quot;XYZ Enterprises Limited is a company registered in England and Wales with company number 1234567&quot;)</li>
      <li>If the business is a member of a trade or professional association, membership details, including any registration number, should be provided.</li>
      <li>If the business has a VAT number, it should be stated - even if the website is not being used for ecommerce transactions.</li>
      <li>Prices on the website must be clear and unambiguous. Also, state whether prices are inclusive of tax and delivery costs. </li>
    </ul>

